    public void ToGuards(){
        File file = new File(getClass().getResource("Quality.txt").getPath());
        int numGuard =0;
        Scanner sc2 = null;
        try {
            sc2 = new Scanner(file);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Home_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        while(sc2.hasNextLine()) {
	    	String tempLine = sc2.nextLine();
                if(!tempLine.equals("לילה:")){
                    if(tempLine.equals("שבת בוקר:"))
                        break;
                    else
                        numGuard++;
                }
        }
        sc2.close();
        Scanner sc = null;
        try {
            sc = new Scanner(file);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Home_Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
         int k=0;
            guards = new Guard[numGuard];
	    authority_temp = new String[guards.length];
	    for(int i=0;i<authority_temp.length;i++)
	    	authority_temp[i] = "";
	    quality_temp = new String[guards.length][3];
	    for(int i =0 ;i<quality_temp.length;i++)
	    	for(int j=0;j<quality_temp[i].length;j++)
	    	quality_temp[i][j] = "";
	    boolean number = false;
	    for(int i=0;i<guards.length;i++)
	    	guards[i] = new Guard();
	    int kind =0;
	    while(sc.hasNextLine()) {
	    	String tempLine = sc.nextLine();
	    	switch(tempLine) {
	    	case "שבת בוקר:":
	    		kind = 1;
	    		k=0;
	    		number = false;
	    		break;
	    	case "מוצ\"ש/שישי לילה:":
	    		kind = 2;
	    		k=0;
	    		number = false;
    			break;
	    	case "אחמ\"שים:":
	    		k=0;
	    		kind = 3;
	    		break;
	    	case "קצין מתקן:":
	    		kind = 4;
	    	}
	    	if(kind ==0) {
	    		if(!tempLine.equals("לילה:")) {
	    			for(int i=0;i<tempLine.length();i++) {
	    				char tav = tempLine.charAt(i);
	    				if(tav >= '0' && tav <= '9') {
	    					quality_temp[k][0] += tav; 
	    			    	number = true;
	    				}
	    				else {
	    					if(tav != '-' && tav != ' ') {
	    			    		if(number) {
	    			    			k++;
	    			    			number =false;
	    			    		}
	    			    		guards[k].setName(guards[k].getName() + tav); 
	    				}
	    			}
	    		}
	    	}
	    	}
	    	else {
	    		if(kind ==1) {
	    			if(!tempLine.equals("שבת בוקר:")) {
	    			for(int i=0;i<tempLine.length();i++) {
	    				char tav = tempLine.charAt(i);
	    				if(tav >= '0' && tav <= '9') {
	    					quality_temp[k][1] += tav; 
	    			    	number = true;
	    				}
	    				else {
	    					if(tav != '-' && tav != ' ') {
	    			    		if(number) {
	    			    			k++;
	    			    			number =false;
	    			    		}
	    				}
	    			}
	    		}
	    		}
	    		}
	    		else {
	    			if(kind ==2) {
	    			if(!tempLine.equals("מוצ\"ש/שישי לילה:")) {
	    			for(int i=0;i<tempLine.length();i++) {
	    				char tav = tempLine.charAt(i);
	    				if(tav >= '0' && tav <= '9') {
	    					quality_temp[k][2] += tav; 
	    			    	number = true;
	    				}
	    				else {
	    					if(tav != '-' && tav != ' ') {
	    			    		if(number) {
	    			    			k++;
	    			    			number =false;
	    			    		}
	    				}
	    			}
	    		}
	    		}
	    			}
	    			else {
	    				if(kind ==3) {
	    				if(!tempLine.equals("אחמ\"שים:")) {
	    					authority_temp[k] = tempLine;
	    					k++;
	    				}
	    				}
	    				else {
	    					if(!tempLine.equals("קצין מתקן:")) {
	    						officer = tempLine;
	    					}
	    				}
	    			}
	    		}
	    	}
	    }
	    sc.close();
            for(int i=0;i<guards.length;i++) {
	    	guards[i].setName(guards[i].getName().replaceAll(System.getProperty("line.separator"), "")); 
	    	if(is_in_array(authority_temp, guards[i].getName()))
	    		guards[i].setAuthority(true);
	    }
            //String To number
	    for(int j=0;j<guards.length;j++) {
	    	guards[j].setNight(StringToInt(quality_temp[j][0]));
	    }
	    for(int j=0;j<guards.length;j++) {
	    	guards[j].setSat_morning(StringToInt(quality_temp[j][1]));
	    }
	    for(int j=0;j<guards.length;j++) {
	    	guards[j].setSat_night(StringToInt(quality_temp[j][2]));
	    }
    }
