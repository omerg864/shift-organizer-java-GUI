    public boolean isInTexts(String text,String name){
            if(!text.equals("")){
            String[] first = text.split("\n");
            for(int i=0;i<first.length;i++){
                first[i] = RemoveSpace(first[i]);
            }
            String[][] split = new String[first.length][];
            for(int i=0;i<first.length;i++){
                split[i] = first[i].split(",");
            }
            for(int i=0;i<split.length;i++){
                for(int j=0;j<split[i].length;j++){
                    split[i][j] = RemoveSpace(split[i][j]);
                    if(split[i][j].equals(name))
                        return true;
                }
            }
        }
            return false;
    }

